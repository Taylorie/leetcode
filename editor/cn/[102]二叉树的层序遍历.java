import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;
import java.util.Queue;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {
    public List<List<Integer>> levelOrder(TreeNode root) {
        List<List<Integer>> res = new ArrayList<>();
        if (root == null) return res;
        dfs(root, res, 0, new ArrayList<Integer>());
        return res;
    }

    private void dfs(TreeNode root, List<List<Integer>> res, int index, ArrayList<Integer> list) {
        if (root == null) return;
        if (res.size() - 1 < index) res.add(new ArrayList<Integer>());
        res.get(index).add(root.val);
        dfs(root.left, res, index+1, list);
        dfs(root.right, res, index+1, list);
    }

}
